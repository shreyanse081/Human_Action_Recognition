def generate_batches(path, batch_size):
    batches = []
    labels = []
    for j in range(80):
        for i in classes:
            p = os.path.join(path, i)
            m = os.path.join(p, 'rgb' + str(j) + '.npy')
            video = np.load(m)[:, :, :, ::-1] / 255.
            batches.append(video)
            labels.append(one_hot[i])
            if len(batches) == batch_size:
                batches = np.array(batches)
                labels = np.array(labels)
                yield batches, labels
                del  batches, labels
                gc.collect()
                batches = []
                labels = []

